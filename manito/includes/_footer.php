						stagePadding: 90
					},
					500: {
						stagePadding: 140
					},
					600: {
						items: 2,
						stagePadding: 10
					}
				}
			};

			if($(window).width()<768){
				$owlPromos.owlCarousel(owlSettings);
				isCarousel = true;
			}

			$(window).resize(function(){
				if($(window).width()<768){
					if(isCarousel==false){
						$owlPromos.owlCarousel(owlSettings);
						isCarousel = true;
					}
				}
				else{
					if(isCarousel==true){
						$owlPromos.owlCarousel('destroy');
						isCarousel = false;
					}
				}
			});

			$('#flex-carousel').flexslider({
				animation: "slide",
				directionNav: true,
				controlNav: false,
				animationLoop: false,
				slideshow: false,
